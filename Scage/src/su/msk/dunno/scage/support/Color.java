	private float[] color_array;
		
	public Color(float red, float green, float blue)
	{
		color_array = new float[3];
		if(red > 1 && red < 256)color_array[0] = red/256;
		else color_array[0] = red;
		if(green > 1 && green < 256)color_array[1] = green/256;
		else color_array[1] = green;
		if(blue > 1 && blue < 256)color_array[2] = blue/256;
		else color_array[2] = blue;
	}
	
	public float getRed()
	{
		return color_array[0];
	}
	
	public float getGreen()
	{
		return color_array[1];
	}
	
	public float getBlue()
	{
		return color_array[2];
	}
